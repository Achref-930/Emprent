/**
 * getShippingFee(wilayaCode, communeName, deliveryType)
 * Returns the shipping fee for a given wilaya + commune + delivery type.
 * Falls back to the wilaya base rate if the commune is not found.
 *
 * @param {string} wilayaCode   - e.g. "31"
 * @param {string} communeName  - e.g. "Oran"
 * @param {"domicile"|"desk"} deliveryType
 * @returns {number} fee in DA
 */
export function getShippingFee(wilayaCode, communeName, deliveryType) {
  const wilaya = WILAYAS.find((w) => w.code === wilayaCode);
  if (!wilaya) return 0;

  const commune = wilaya.communes.find(
    (c) => c.name.toLowerCase() === communeName.toLowerCase(),
  );

  const source = commune || wilaya;
  return deliveryType === "desk" ? source.desk : source.domicile;
}
